package com.himanshu.infirment.model;

public class VideoItem {

    String url;
    String time;

    public VideoItem() {
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
